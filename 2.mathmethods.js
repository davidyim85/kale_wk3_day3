// Operators

// + addition +
console.log(1 + 1);

// - subtraction -
console.log(1 -1);

// Multiplication or x *
console.log(2 * 2)

// look up PEMDAS or order of operations

// Exponentiation **
console.log(2 ** 3)

// Division /
console.log(6 / 4)

// Modulus %
// Division remainder
console.log(5 % 3)

// Increment ++
let x = 101;
x++
console.log(x)

// Decrement --
let y = 101;
y--
console.log(y)


// Increment by value
let z = 101;
z+=10
console.log(z)

// Decrement by value -=
let aa = 101;
aa-=10
console.log(aa)



